	        <h1>Business Potential</h1>
	        <h6>
	        	
	        	This idea, however simple it may sound, has amazing potential, assuming I take 1% fee on all posted jobs. Questr is like Uber but for any sort of transaction. 
	        	Possible jobs can include mowing the lawn, plumbing, math homework help, shoveling the snow, etc. 

	        	<br><br>

	        	...But, here is why this idea is so great. A $10 task earns me $0.01, and a $1000 task earns me $10. This would be good for people who are unemployed or can only find part time work. There is a huge chunk of the market that is priced out of the economy due to minimum wage laws. Students would also greatly benefit from this.
	        	There is also potential to put reviews/jobs done on resume for experience. This application also connects people. It will channel the supply side towards the demand side.

	        	<br><br>
	        	Consider the following. Thinking <strong>extremely</strong> conservatively, lets say I launch this website and mobile application in Toronto, which has a population of <strong>2.62 million</strong>. Let's assume only <strong>1%</strong> of the people use this app to post quests. Thats <strong>26,200</strong> people. Lets say they do <strong>one</strong> transaction in <strong>one</strong> year (in reality they will do multiple, but lets keep it real simple). Let's also assume <strong>70%</strong> of those requests are fulfilled, and completed. That is <strong>18,300</strong> requests completed. Let's say the average transaction was <strong>$10</strong>. That is <strong>$183,000</strong> in transactions. That is <strong>$1,830</strong> in fees from <strong>one</strong> city, with <strong>extremely</strong> conservative assumptions.
	        	<br>
	        	Looking at a global scale, there are <strong>3.3 billion</strong> internet users today, and the number is only going to go up. But let's say <strong>1%</strong> of this group posts <strong>one</strong> transaction a year. That's <strong>33.4 million</strong> requests. With <strong>70%</strong> fulfillment, that's <strong>23.4 million</strong> transactions. At <strong>$10</strong> each transaction, we have a grand total of <strong>$234.2 million</strong> in transaction volume, which evaluates to <strong>$2.3 million</strong> in fees.<br>
	        	Again, this is assuming <strong>one transaction per person per year</strong> at a lowball $10 transaction. The economic benefits would be huge as well because this would fill a huge gap in unemployment. More work for people, more money for them to spend, more experience. If this idea takes off, we are looking at millions, if not billions in profit.

